// Default municipalities for other wilayas if not detailed
export const getDefaultMunicipalities = (wilayaId) => {
  if (MUNICIPALITIES[wilayaId]) return MUNICIPALITIES[wilayaId];
  
  const wilaya = WILAYAS.find(w => w.id === wilayaId);
  if (!wilaya) return [];
  
  return [{ id: `${wilayaId}01`, name: wilaya.name, name_ar: wilaya.name_ar }];
};
